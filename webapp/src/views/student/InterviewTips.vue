        <b>1. Have strategic follow-up questions for your interviewer</b>
        <p>You’re probably aware that you should ask questions when prompted in an interview. Not doing so is sure to make you stand out in a negative way. However, asking the right questions can make the interviewer(s) remember you in a positive light. The questions that you ask in an interview are another tool to show how impressive you are. Educated questions based on your future responsibilities illustrate how prepared you are to start working.</p>

        <b>2. Study commonly asked questions for your job field</b>
        <p>The probability that an interviewer will stick to completely unique questions is small. Naturally, certain questions can become quite popular among interviewers, especially among those in the same field. If you research these questions beforehand you are either guaranteed to be more prepared for the interview, or more prepared for the job you’re interviewing for.</p>

        <b>3. Read-up on the company that your interviewing for</b>
        <p>As was mentioned before, asking questions is important in an interview. While an employer is unlikely to quiz you on the company, being able to speak intelligently on the company and its purpose goes further to show how prepared you are for the job. When you’re asking questions, be sure to articulate a solid understanding of how your work can impact the company.</p>

        <b>4. Dress appropriately</b>
        <p>It is important to wear clothes that present you as a serious professional to your interview. In general, you probably don’t have to worry about overdressing, but feel free to ask you recruiter what attire is most appropriate.</p>

        <b>5. Relax</b>
        <p>Confidence makes people think that you know what you’re talking about, and as long as you’re completely honest on your resume you should. If you make it to an interview the company is seriously considering you as a candidate so there’s no need to be anything other than your best self. Everyone gets nervous (including your interviewer), just don’t let your nerves come off as uncertainty.</p>

